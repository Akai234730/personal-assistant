# ============================================================
# agents/translate.py - 翻译 Agent
# ============================================================
# 开发顺序：第二个写，逻辑最简单（不需要外部 API）
# 原理：完全依靠 GPT 的语言能力，通过精心设计的提示词
#       让 GPT 提供直译+意译两个版本
# ============================================================

from app.services.openai_client import chat_with_ai


def translate_agent(text: str, target_language: str, scene: str) -> str:
    """
    翻译 Agent：输入原文+目标语言+场景，输出多版本翻译
    """
    system_prompt = f"""你是精通多国语言的专业翻译和语言学家。
翻译风格根据场景自动调整：
- 日常对话：自然流畅，口语化
- 正式文件：严谨规范，措辞正式
- 技术文档：精准专业，保留术语
- 文学作品：优美动人，保留韵味

当前场景：{scene}
目标语言：{target_language}

请按以下格式输出（使用 Markdown）：

**📝 直译版**
（逐字逐句，忠实原文）

**✨ 意译版**
（更自然流畅，符合目标语言表达习惯）

**💡 翻译说明**
（说明关键词处理或文化差异，无特别说明则省略）"""

    # temperature=0.3 较低，翻译需要准确，不要过于「创意」
    return chat_with_ai(system_prompt, f"请翻译：\n\n{text}", temperature=0.3)
